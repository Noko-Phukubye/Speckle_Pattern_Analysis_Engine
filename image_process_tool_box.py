        read_path = os.path.join(read, file_name)
        # image = cv2.imread(read_path, cv2.IMREAD_GRAYSCALE)
        image = readImage(read_path)
        
        # Invert the image (255 - pixel value)
        inverted_image = np.max(image) - image
        
