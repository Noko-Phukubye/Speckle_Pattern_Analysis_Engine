# Image power spectrum (https://www.cs.unm.edu/~brayer/vision/fourier.html)
def compute_fft_and_freq(image):
    '''
    This function computes the Fourier transform of an image with the 
    DC componenent (average grey value) moved to the centre. The function 
    also calculates the magnitude of the complex components and 
    subsequently calculates the spectral power of the image which is 
    used in the thesis as a speckle pattern quality metric

    Details on 2D-DFT can be found in resources like 'Digital image processing by
    Gonzalez et al (2004)

    '''

    rows, cols = image.shape
    fft2d = np.fft.fft2(image)              # Compute unnormalised FFT
    # Shift the zero frequency (mean grey value) to the center of the image spectrum
    fft2d_shifted = np.fft.fftshift(fft2d)  
    magnitude = np.abs(fft2d_shifted)
    power = magnitude ** 2

    rows, cols = image.shape
    fx = np.fft.fftshift(np.fft.fftfreq(cols))
    fy = np.fft.fftshift(np.fft.fftfreq(rows))  

    return magnitude, power, fx, fy


#-------------------------------------------------
# Area under power spectrum using Simpsons Method
#-------------------------------------------------
def integrate_power_2d(power, fx, fy):
    """
    2D integration using Simpson's rule
    """
    dfx = fx[1] - fx[0] if len(fx) > 1 else 1.0
    dfy = fy[1] - fy[0] if len(fy) > 1 else 1.0
    
    # Simpson's rule integration
    integral = simpson(simpson(power, dx=dfx, axis=1), dx=dfy, axis=0)
    return integral

