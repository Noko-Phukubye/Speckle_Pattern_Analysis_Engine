    p_metrics = np.zeros((maxrow, active_metric_columns))
    p_param = np.zeros((maxrow, active_parameter_columns))
    meas_error = np.zeros((maxrow, active_error_columns))
    indicators = np.empty((3, 1), dtype=object)  # Expanded to 3 for Turing data

    nans = np.zeros((maxrow, 1))
    
    # Check if columns W (23) and X (24) are filled with Turing parameters
    turing_params_exist = False
    turing_radius_data = np.zeros((maxrow, 1))
    turing_reps_data = np.zeros((maxrow, 1))
    
    w_header = sheet_obj.cell(row=1, column=23).value
    x_header = sheet_obj.cell(row=1, column=24).value
    
    if w_header is not None and x_header is not None:
        first_w_value = sheet_obj.cell(row=2, column=23).value
        first_x_value = sheet_obj.cell(row=2, column=24).value
        
