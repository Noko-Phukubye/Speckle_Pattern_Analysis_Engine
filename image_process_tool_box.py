# Get list of image names
#----------------------------------------------------
def get_image_strings(path, imagetype='tif', parity='even'):

    """
    This function reads a folder and returns a list containing 
    the names of the folder's contents in the form of strings.
    Only qualifying strings are returned. To qualify the string must 
    end with the user-specified file extension and the first 
    part of the name (when the name is split using underscores) 
    should be either an even or odd digit.

    Parameters: 
        -   Path: A string indicating the directory of files
        -   imagetype:  A string indicating the qualifying file extension
        -   parity: A string for toggling between the choice of returning even or odd-numbered strings
    
    Returns:
        -   A list object of sorted strings
    """

    if parity.lower() == 'even':
        # Get files from directory that end with 'imagetype' 
        # and start with an even number
        return sorted(
            (f for f in os.listdir(path) 
            if f.endswith(f".{ imagetype.lower() }") and f.split('_')[0].isdigit() 
            and int(f.split('_')[0]) % 2 == 0),
            key=lambda x: int(x.split('_')[0])
        )
    elif parity.lower() == 'odd':
        return sorted(
        (f for f in os.listdir(path) 
         if f.endswith(f".{ imagetype.lower() }") and f.split('_')[0].isdigit() 
         and int(f.split('_')[0]) % 2 != 0),
        key=lambda x: int(x.split('_')[0])
        )
    elif parity.lower() == 'none':
        return sorted(
        (f for f in os.listdir(path) 
         if f.endswith(f".{ imagetype.lower() }") and f.split('_')[0].isdigit()),
        key=lambda x: int(x.split('_')[0])
        )
    else:
        raise Exception("Invalid parity selected")


