def array_difference_outlier(array_1, array_2, thresh):
    """
    Compares two arrays element-by-element and returns the indices where
    the absolute difference between corresponding values exceeds a threshold.

    Used to flag patterns where the d2f and f2d RMSE values diverge significantly,
    which typically indicates a poorly correlated or unreliable DIC result.

    Args:
        array_1 : first array of values (e.g. RMSE_d2f)
        array_2 : second array of values (e.g. RMSE_f2d)
        thresh  : maximum acceptable absolute difference between paired values

    Returns:
        outlier_idx : list of indices where abs(array_1[i] - array_2[i]) > thresh
    """
    outlier_idx = []

    for i in range(len(array_1)):
        if np.abs(array_1[i] - array_2[i]) > thresh:
            outlier_idx.append(i)

    return outlier_idx



