def make_xl(excel_path, file_name='custom_doc'):
    """
    Creates a new Excel document to store data.

    The filename is based on the number of existing files in the directory
    with the same base name to avoid overwriting.
    """
    print("\n1. Creating Excel document...\n")

    if not os.path.exists(excel_path):
        os.makedirs(excel_path)
        doc_number = 1
    else:
        existing_files = [f for f in os.listdir(excel_path)
                          if f.startswith(file_name) and f.endswith(".xlsx")]
        doc_number = len(existing_files) + 1

    print("Current doc_number:", doc_number)
    exl_file_name = f"{file_name}_{doc_number}.xlsx"
    exl_file_path = os.path.join(excel_path, exl_file_name)

    workbook = openpyxl.Workbook()
    workbook.save(exl_file_path)
    del workbook

    return exl_file_path


