# Mean intensity gradient
#---------------------------------------------------------------------------
def MIG(image2):
    """
    Calculate the Mean Intensity Gradient (MIG) for an image.
    Parameters:
        image (ndarray): Input image (grayscale).
    Returns:
        float: The MIG value for the image.
    """
    
    # Store the size
    nY = image2.shape[0]
    nX = image2.shape[1]

    # Calculate the gradients with respect to rows and columns
    dfRows, dfCols = np.gradient(image2)
    # Calcuate the L2 norm of the two gradient arrays (basically the modulus of the local intensity gradient vector)
    df = np.sqrt( (dfRows**2 + dfCols**2) )
    # Now calculate the MIG value
    mig = np.sum(df) / (nY*nX)
    return mig


