def cv2_deform(image, source_points = None, destination_points = None, 
               kern = 3, dx = None, dy = None):
    """
    Deforms an image using a displacement field generated by 
    Radial Basis Function (RBF) interpolation.

    If dx and dy are not provided, the displacement field is 
    computed from source and destination points 
    (which must be provided in that case).
    The deformation is then applied using scipy.ndimage.map_coordinates.

    This function is similar to the one that uses map_coordinates with the 
    main difference being the pixel remapping. This function uses 
    CV2-based pixel remapping which has more interpolation options. 
    
    Parameters:
        image (ndarray): Input image (grayscale or RGB).
        source_points (ndarray): Nx2 array of original coordinates 
        (optional if dx/dy are provided).
        destination_points (ndarray): Nx2 array of deformed coordinates 
        (optional if dx/dy are provided).

        kern (int): Kernel type for RBF interpolation:
                    1 = thin_plate_spline, 2 = linear, 3 = cubic, 4 = quintic.
        dx (ndarray): Precomputed x-displacement field (optional).
        dy (ndarray): Precomputed y-displacement field (optional).

    Returns:
        remapped_image (ndarray): Deformed image.
        difference_image (ndarray): Absolute pixel-wise difference from original.
    """
    # Image size
    h, w = image.shape[:2]
    # Create a grid of coordinates (destination grid)
    grid_x, grid_y = np.meshgrid(np.arange(w), np.arange(h))
    grid_x_flatten = grid_x.flatten()
    grid_y_flatten = grid_y.flatten()

    # If interp field is not provided already, the function generates a smooth field from provided nodal coordinates
    if dx is None and dy is None:
        
        kernel_options = {1: 'thin_plate_spline', 2: 'linear', 3: 'cubic', 4: 'quintic'}
        print("Running radial basis function interpolation with kernel: ", kernel_options.get(kern))

        kerne = kernel_options.get(kern)
        if kerne is None:
            raise ValueError("Invalid kernel option. Use 1 for thin_plate_spline, 2 for linear, 3 for cubic, or 4 for quintic.")
        
        # Create an interpolator for each coordinate direction 
        rbf_interpolator_x = RBFInterpolator(source_points, 
                                             destination_points[:, 0] - source_points[:, 0], 
                                             kernel=kerne)
        rbf_interpolator_y = RBFInterpolator(source_points, 
                                             destination_points[:, 1] - source_points[:, 1], 
                                             kernel=kerne)
        '''
        Apply RBF interpolation to get a smooth displacement field. The displacement field x and y components are 
        reshaped to the image dimensions by stacking the grid coordinates and then reshaping the result. The displacement
        field is interpolated at the grid coordinates.
        '''
        dx = rbf_interpolator_x(np.column_stack([grid_x_flatten, grid_y_flatten])).reshape(h, w)
        dy = rbf_interpolator_y(np.column_stack([grid_x_flatten, grid_y_flatten])).reshape(h, w)
    #--------------------------------------------------
    # Create the remap coordinates (cv2 uses float32) --  would astype('uint8') work?
    # It probably would be better if I used it instead seeing that scikit-ndimage uses it
    # for 8-bit images in ndarrys
    # 	The first map of either (x,y) points or just x values having the type CV_16SC2 , CV_32FC1, or CV_32FC2

    map_x = (grid_x - dx).astype(np.float32)
    map_y = (grid_y - dy).astype(np.float32)
    #--------------------------------------------------

    # Remap the image using OpenCV and interpolation. The possible interpolation kernels/orders are:
    # cv::INTER_NEAREST = 0,
    # cv::INTER_LINEAR = 1,
    # cv::INTER_CUBIC = 2,
    # cv::INTER_AREA = 3,
    # cv::INTER_LANCZOS4 = 4,
    # cv::INTER_LINEAR_EXACT = 5,
    # cv::INTER_NEAREST_EXACT = 6,
    # cv::INTER_MAX = 7,
    # cv::WARP_FILL_OUTLIERS = 8,
    # cv::WARP_INVERSE_MAP = 16

    interp = cv2.INTER_LANCZOS4
    print("Remapping image using OpenCV...")
    if len(image.shape) == 2:  # Grayscale image
        remapped_image = cv2.remap(image, map_x, map_y, 
                                 interpolation=interp,
                                 borderMode=cv2.BORDER_CONSTANT)
    else:  # Color image
        remapped_image = cv2.remap(image, map_x, map_y, 
                                 interpolation=interp,
                                 borderMode=cv2.BORDER_CONSTANT)
        
    original_float = image.astype(np.float32)
    remapped_float = remapped_image.astype(np.float32)
    difference_image = np.abs(original_float - remapped_float)

    return remapped_image, difference_image



