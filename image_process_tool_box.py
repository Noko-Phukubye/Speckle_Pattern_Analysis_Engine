def ordered_prefix(folder_with_images,refer = 'ref', image_type = 'tif', start_at = 0, intervals=2):
    """
    Adds or changes the prefixes on relevant file names. The new, intervaled prefixes follow
    an ascending order

    Returns None
    """
    # Get list of images (strings)
    if refer.lower() == 'ref':
        image_files = sorted(
            [f for f in os.listdir(folder_with_images) if f.endswith(f".{image_type}") 
             and f.split('_')[0].isdigit() and int(f.split('_')[0]) % 2 == 0],
            key = lambda x: int(x.split('_')[0])
        )
    else:
        if start_at == 0:
            start_at = 1

        image_files = sorted(
            [f for f in os.listdir(folder_with_images) if f.endswith(f".{image_type}") 
             and f.split('_')[0].isdigit() and int(f.split('_')[0]) % 2 != 0],
            key = lambda x: int(x.split('_')[0])
        )

    # From that list, change the first string element (separated by '_') to appropriate number
    end_at = start_at + intervals * len(image_files)
    prefixes = np.arange(start_at, end_at, intervals)

    for i, file in enumerate(image_files):

        parts = file.split('_')
        parts[0] = str(prefixes[i])
        new_filename = '_'.join(parts)
        old_path = os.path.join(folder_with_images, file)
        new_path = os.path.join(folder_with_images, new_filename)
        if os.path.exists(new_path):
            print(f"Skipping rename: {new_filename} already exists.")
            continue
        os.rename(old_path, new_path)
        print(f"Renamed: {file} → {new_filename}")

    return None


