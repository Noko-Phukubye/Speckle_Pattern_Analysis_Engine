# E_f parameter
#---------------------------------------------------------------------------
def Ef(image):

    """
    Calculates the parameter Ef which considers image gradients and the second derivative.
    Reference: Hu et al, 2021

    Input: grey image
    Output: float

    """
    # First derivative (gradient) along the x and y axes
    dy, dx = np.gradient(image)
    # Second derivative (gradient of gradients) along the x and y axes
    ddy = np.gradient(dy, axis=0)  # Second derivative along the rows (y-axis)
    ddx = np.gradient(dx, axis=1)  # Second derivative along the columns (x-axis)

    # Calculate kx and ky
    epsilon = 1e-10
    kx = np.sum(np.abs(ddx * dx)) / (np.sum(dx**2) + epsilon)
    ky = np.sum(np.abs(ddy * dy)) / (np.sum(dy**2) + epsilon)
    # Ef value
    Ef = np.sqrt(kx**2 + ky**2)

    return Ef


