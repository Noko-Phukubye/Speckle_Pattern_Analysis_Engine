def difference_image(original,deformed, image_reference = 0, wait = 7.5, save_loc = None):

    ''' Generate a jet coloured image showing the normalised differences in pixel intensities 
        between two images'''

    # Convert the 8bit images to floating point arrays for easier manipultation
    original_float = original.astype(np.float32)
    remapped_float = deformed.astype(np.float32)
    difference_image = np.abs(original_float - remapped_float)

    # Showing the difference image
    if difference_image.ndim == 3:
        difference_image = np.mean(difference_image, axis=2)

    # Normalise the differences to [0,1]
    normalised_difference = difference_image / difference_image.max()

    #-------------------------------------------------------------
    # Create the figure and display
    plt.figure(figsize=(6, 5))
    plt.title("Difference image")
    img = plt.imshow(normalised_difference, cmap='jet')
    plt.colorbar(label="Difference")
    plt.axis('off')
    plt.tight_layout()

    # Save the image if indicated by the user
    if save_loc is not None:
        # Save the figure exactly as displayed (including colormap)
        difference_img_save_path = os.path.join(save_loc, f'{image_reference}_difference_image.png')
        plt.savefig(difference_img_save_path, bbox_inches='tight', pad_inches=0, dpi=300)

    # Show and then close
    plt.show(block=False)
    plt.pause(wait)
    plt.close()

    #-------------------------------------------------------------
    # Collapsed differences. Normalised pixels averaged along the y-columns
    collapsed_difference = np.mean(difference_image, axis=0)
    # Generate the x-line (pixel positions along the columns of the image)
    x_line = np.arange(difference_image.shape[1])  # This corresponds to the column indices (width of the image)
    # Plotting the collapsed error (difference) along axis 0 as a line graph
    plt.figure(figsize=(10, 6))
    plt.plot(x_line, collapsed_difference, color='blue', linewidth=1.5)
    plt.title("Collapsed Difference Image Along Axis 0")
    plt.xlabel("Pixel Column Index (X)")
    plt.ylabel("Mean Pixel Difference")
    plt.grid(True)

    if save_loc is not None:
        # Save the plot before closing it
        slice_save = os.path.join(save_loc, f'{image_reference}_collapsed_difference_image_axis0.png')  # Adjust path as needed
        plt.savefig(slice_save)

    plt.show(block=False)
    plt.pause(wait)
    plt.close()

    #-------------------------------------------------------------
    # Slice through the normalised difference image at the y = height / 2 line
    row_index =  difference_image.shape[0] // 2
    # Slice the difference image at the specific row
    row_difference = difference_image[row_index, :]  
    # Generate the x-line (pixel positions along the columns of the selected row)
    x_line = np.arange(difference_image.shape[1]) 
    # Plotting the pixel differences along the selected row
    plt.figure(figsize=(10, 6))
    plt.plot(x_line, row_difference, color='blue', linewidth=1.5)
    plt.title(f"Difference Image Slice at Row {row_index}")
    plt.xlabel("Pixel Column Index (X)")
    plt.ylabel("Pixel Difference")
    plt.grid(True)
    # Save the plot before closing it
    if save_loc is not None:
        slice_save = os.path.join(save_loc, f'{image_reference}_slice_row_{row_index}_difference_image.png') 
        plt.savefig(slice_save)
    plt.show(block=False)
    plt.pause(wait)
    plt.close()



