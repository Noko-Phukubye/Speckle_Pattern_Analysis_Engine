def move_files(read, write, image_type='tif', suffix='threshold_threshold'):

    print(f'\nWill save to:{write}...\n')
    time.sleep(5)
    '''
    Moves files ending with a specific suffix (e.g., 'threshold_threshold.tif')
    from the read folder to the write folder.
    '''
    if not os.path.exists(write):
        os.makedirs(write)

    image_files = sorted(
        [f for f in os.listdir(read) if f.endswith(f"{suffix}.{image_type}")],
        key=lambda x: int(x.split('_')[0]) if x.split('_')[0].isdigit() else float('inf')
    )

    for file_name in image_files:
        source_path = os.path.join(read, file_name)
        dest_path = os.path.join(write, file_name)
        shutil.move(source_path, dest_path)

    return None


