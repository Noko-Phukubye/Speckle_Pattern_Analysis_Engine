def super_autocorr(image1, image2, image3=None, image4=None, image5=None, 
                   dx_min=-60, dx_max=60, meth=4, cardinality=100, image_1_name = None, 
                   image_2_name = None, legg = False):
    """
    Function calculates the autocorrelation of two or three images and superimposes their graphs as one figure.

    Input
        image1 : ndarray First image to be correlated
        image2 : ndarray Second image to be correlated
        image3 : ndarray Optional third image to be correlated (default: None)
        dx_min : integer representing the lower limit of pixels to which the image will be shifted
        dx_max : integer representing the upper limit of pixels to which the image will be shifted
        meth   : method index for template matching
        cardinality : number of shifts to consider for the correlation graph

    Output
        figure: The resulting autocorrelation distribution figure
    """
    time1 = time.time()

    # Methods dictionary
    methods = {
        1: 'TM_CCOEFF',
        2: 'TM_CCOEFF_NORMED',
        3: 'TM_CCORR',
        4: 'TM_CCORR_NORMED',
        5: 'TM_SQDIFF',
        6: 'TM_SQDIFF_NORMED'
    }

    method = getattr(cv2, methods.get(meth))
    print("\nAutocorrelation analysis: method", methods.get(meth))

    # Convert images to grayscale if needed
    img1 = cv2.cvtColor(image1, cv2.COLOR_BGR2GRAY) if image1.ndim > 2 else image1
    img2 = cv2.cvtColor(image2, cv2.COLOR_BGR2GRAY) if image2.ndim > 2 else image2
    img3 = cv2.cvtColor(image3, cv2.COLOR_BGR2GRAY) if image3 is not None and image3.ndim > 2 else image3
    img4 = cv2.cvtColor(image4, cv2.COLOR_BGR2GRAY) if image4 is not None and image4.ndim > 2 else image4
    img5 = cv2.cvtColor(image5, cv2.COLOR_BGR2GRAY) if image5 is not None and image5.ndim > 2 else image5


    # Define the shifts for the correlation analysis
    image_shifts = np.linspace(dx_min, dx_max, cardinality)

    # There are as many average correlation values as there are shifts
    correlation_value1 = np.zeros(len(image_shifts))
    correlation_value2 = np.zeros(len(image_shifts))
    correlation_value3 = np.zeros(len(image_shifts)) if image3 is not None else None
    correlation_value4 = np.zeros(len(image_shifts)) if image4 is not None else None
    correlation_value5 = np.zeros(len(image_shifts)) if image5 is not None else None

    # Compute correlation for both images
    # For each image, it generates n translated cases where n = len(image_shifts)
    # For each images it creates a vector of maximum corretion values, the vector length = len(image shifts)
    for i, shift in enumerate(image_shifts):

        # Shift the first image and save the average correlation value
        template1 = translate_image(img1, shift, 0)
        res1 = cv2.matchTemplate(img1, template1, int(method))
        correlation_value1[i] = np.mean(res1)

        # Shift the second image
        template2 = translate_image(img2, shift, 0)
        res2 = cv2.matchTemplate(img2, template2, int(method))
        correlation_value2[i] = np.mean(res2)

        # Shift the third image if provided
