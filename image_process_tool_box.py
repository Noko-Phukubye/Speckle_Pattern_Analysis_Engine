def translate_image(image, u, v):

    """Apply translation to image using scipy's map_coordinates and interpolation.
    
    Args:
        image: 2D numpy array
        u: horizontal translation (in pixels) - positive u moves content RIGHT
        v: vertical translation (in pixels) - positive v moves content DOWN
        order: interpolation order (0=nearest, 1=bilinear, 3=bicubic, etc)
    
    Returns:
        Translated image
    """
    height, width = image.shape
    x, y = np.meshgrid(np.arange(width), np.arange(height))
    
    # Apply translation to grid (corrected sign for intuitive direction)
    x_displaced = x - u  
    y_displaced = y - v
    
    # Create coordinate array for map_coordinates
    new_coordinates = np.array([y_displaced, x_displaced])
    
    # Interpolate with reflect mode for better boundary handling
    order = 3
    translated = map_coordinates(
        image, 
        new_coordinates, 
        order=order, 
        mode='constant', 
        prefilter=True
        )
    
    # return translated.reshape(image.shape)
    return translated


