# Generate one Perlin noise image
#-------------------------------------------------------------
def generate_single_perlin_image(h, w, scale, octaves, persistence, 
                                 lacunarity,texture_function="none", seed=None):
    """
    Generate a single Perlin noise image with specified parameters and save it.
    
    Parameters:
        h, w              : Image height and width
        scale             : Perlin noise scale (frequency)
        octaves           : Number of noise octaves (int)
        persistence       : Persistence value
        lacunarity        : Lacunarity value
        save_path         : Directory to save image
        texture_function  : Texture type to apply (string)
        seed              : Random seed for repeatability (optional)
        filename          : Name of the output image file
    """
    
    if seed is not None:
        np.random.seed(seed)

    # Create coordinate grid
    y, x = np.meshgrid(np.arange(h), np.arange(w))

    # Generate Perlin noise image
    template = np.zeros((h, w), dtype=np.float32)
    for row in range(h):
        for col in range(w):
            template[row, col] = pnoise2(col * scale, row * scale,
                                         octaves=int(round(octaves)),
                                         persistence=persistence,
                                         lacunarity=lacunarity,
                                         repeatx=w, repeaty=h,
                                         base=seed or 0)

    # Normalize to 0–255
    template = ((template - template.min()) / (template.max() - template.min()) * 255).astype(np.uint8)

    # Apply texture
    template = apply_texture(template=template, texture_type=texture_function)

    return template


