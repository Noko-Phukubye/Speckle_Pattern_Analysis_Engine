# Sum of square of subset intensity gradients
#----------------------------------------------------------------------------------------------
def globalSSSIG(matt, subset, stride):
    """
    Reference: Pan et al. 2008
    Calculate the global subset gradient sum squared (SSSIG) for an image.
    Parameters:
        image (ndarray): Input image (grayscale).
        subsetsize (int): Size of the subsets.
        stride (int): Stride for moving the subsets.
    Returns:
        float: The global SSSIG value.
    """
    # Parameters
    sub_matrix_size = subset
    stride1 = stride  
    stride2 = stride

    # Calculate shape and strides
    shape = (
        (matt.shape[0] - sub_matrix_size) // stride1 + 1,
        (matt.shape[1] - sub_matrix_size) // stride2 + 1,
        sub_matrix_size,
        sub_matrix_size)

    # Create sub-matrices. The first and second indeces are the 'rows' and 
    # 'columns' of the multidimensional array.
    # These two indeces can be used to access a submatrix. The last two 
    # indices refer to the rows and columns within
    # a specific submatrix.
    sub_matrices = img_subsets(matt, subset, stride)

    # Compute gradients, sqaure them and then append to the sum
    sum_of_squares = 0
    for i in range(sub_matrices.shape[0]):
        for j in range(sub_matrices.shape[1]):
            subset = sub_matrices[i, j]

            # Apply central differencing
            grad_x, grad_y = np.gradient(subset)
            sum_of_squares += np.sqrt(grad_x**2 + grad_y**2)

    # Normalise SSSIG for each subset is determined but now they need 
    # to be aggregated to arrive at a global metric
    # The SSSIGs are summed and normalised by the total number of subsets
    # across the image

    num_subsets = shape[0] * shape[1]
    result = np.sum(sum_of_squares) / num_subsets

    return result


