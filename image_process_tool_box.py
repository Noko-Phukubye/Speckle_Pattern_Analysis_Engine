# General function for obtaining image subsets
#--------------------------------------------------------------------------
def img_subsets(image, subsetsize, stride):
    """
    Divides image into subsets (subimages) using matrix slicing and 
    stores the result as an array of matrices that 
    can be indexed.
    Currently applies the same stride length in both directions.

    credit: https://ipython-books.github.io/46-using-stride-tricks-with-numpy/

    returns matrix of matrices (subsets): rows = rows of subsets
                                          columns = columns of subsets
                                          3rd index = number of rows in a submatrix
                                          4th index = number of columns in submatrix

    Select a subset by indexing the first two entries           -> get_subset = image_subsets[20,20,:,:]
    Select a specific pixel by indexing the last two entries    -> get_pixel_value = image_subsets[20,20,15,15]
    """
    # Check image channels
    if image.ndim > 2:
        img = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    else:
        img = image
     # Parameters
    sub_matrix_size = subsetsize
    stride1 = stride  
    stride2 = stride
    # Calculate shape and strides
    shape = (
        (img.shape[0] - sub_matrix_size) // stride1 + 1,
        (img.shape[1] - sub_matrix_size) // stride2 + 1,
        sub_matrix_size,
        sub_matrix_size
    )
    strides = (
        img.strides[0] * stride1,
        img.strides[1] * stride2,
        img.strides[0],
        img.strides[1]
    )
    # Create sub-matrices
    sub_matrices = as_strided(img, shape=shape, strides=strides)
    return sub_matrices

