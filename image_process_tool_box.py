def compute_bias_prediction(img, plot_spectrum=False, algorithm='FA-NR'):
    """
    Computes bias prediction from a speckle image using transfer functions.
    Based on Yong Su's MATLAB implementation in their 2015 paper.

    Note that the power spectrum calculation here is unnormalised
    to match literature. 

    Parameters:
        img (2D np.ndarray): Grayscale speckle image.
        plot_spectrum (bool): If True, plots the spectrum.
        algorithm (str): 'FA-NR' or 'IC-GN'

    Returns:
        float: Estimated bias (ue)
    """
    
    img = img_as_float(img)
    m, n = img.shape

    # FFT size (zero-padding to next power of 2)
    mfft = 2 ** int(np.ceil(np.log2(m))) 
    nfft = 2 ** int(np.ceil(np.log2(n)))

    # 2D FFT with zero-padding and spectrum shift
    spec = fftshift(fft2(img, s=(mfft, nfft)))

    # Frequency coordinates
    fx = np.linspace(-0.5, 0.5 - 1.0/nfft, nfft)
    fy = np.linspace(-0.5, 0.5 - 1.0/mfft, mfft)
    Fx, Fy = np.meshgrid(fx, fy)

    # Normalized log-magnitude spectrum
    F = np.log(np.abs(spec) + 1)
    F = rescale_intensity(F, in_range='image', out_range=(0, 1))

    if plot_spectrum:
        plt.figure()
        plt.pcolormesh(Fx, Fy, F, shading='auto', cmap='jet')
        plt.xlabel(r'$\nu_x$')
        plt.ylabel(r'$\nu_y$')
        plt.title('Speckle Spectrum')
        plt.axis('equal')
        plt.colorbar(label='Log Spectrum')
        plt.show()

    # Interpolation transfer functions
    def keys_transfer_function(nu):
        """Keys cubic spline transfer function (from MATLAB code)"""
        sinc_nu = np.sinc(nu)
        return sinc_nu**3 * (3*sinc_nu - 2*np.cos(np.pi*nu))
    
    def cubic_bspline_transfer_function(nu):
        """Cubic B-spline transfer function (from MATLAB code)"""
        return 3 * (np.sinc(nu))**4 / (2 + np.cos(2*np.pi*nu))
    
    # Choose transfer function (default to Keys as in MATLAB)
    r = cubic_bspline_transfer_function
    
    # Bias kernel based on algorithm
    if algorithm.upper() == 'FA-NR':
        bias_kernel = ((Fx - 1) * r(Fx - 1) * r(Fy) 
                      - (Fx + 1) * r(Fx + 1) * r(Fy)
                      + r(Fx) * r(Fy) * (r(Fx - 1) * r(Fy) + r(Fx + 1) * r(Fy)))
        
    elif algorithm.upper() == 'IC-GN':

        bias_kernel = ((Fx - 1) * r(Fx - 1) * r(Fy) + Fx * r(Fx) * r(Fy) 
                       + (Fx + 1) * r(Fx + 1) * r(Fy)) * (r(Fx - 1)*r(Fy) - r(Fx + 1)*r(Fy))
    else:
        raise ValueError("Algorithm must be 'FA-NR' or 'IC-GN'")

    # Power spectrum
    power_spectrum = np.abs(spec)**2

    # Integration to compute a and b (matching MATLAB trapz order)
    a = np.trapezoid(np.trapezoid(bias_kernel * power_spectrum, fx, axis=1), fy)
    b = np.trapezoid(np.trapezoid(Fx**2 * r(Fx) * r(Fy) * power_spectrum, fx, axis=1), fy)

    # Bias estimate
    C = a / (2.0 * np.pi * b)
    return C

