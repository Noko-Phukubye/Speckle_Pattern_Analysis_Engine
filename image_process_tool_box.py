# Latin-hypercube sampled parameters
# -------------------------------------------------------------
def gen_lhs_parameters_and_save(parameter_dictionary,number_of_samples, 
                                excel_save_path = None, random_image_save_folder = False,
                                gen_option = 'speckle', image_texture_function = 'none'):
    """
    Function generates samples according to an image generation parameter 
    dictionary and the requested number of sample instances. 
    It returns two objects: a list of even numbers (for naming purposes) 
    and a matrix of image generation 
    parameters. 

    The parameters in a dictionary are limited to 8 for now. 
    The parameters are each saved to a column in an excel document 
    (should the user desire). 
    The parameter values are accessed by whatever image generation 
    function is used. 
    The parameter dictionary must be compatable with the image generation 
    tool's argumnents.

    generator options:      ladisk speckle -> 1
                            ladisk lines   -> 2
                            ladisk checkerboard -> 3
                            Perlin noise -> 4 + texture string 
                            turing -> 4 (based on ladisk speckle parameters with added turing transform)
    """

    """
    Produces sets of values for dictionary-defined parameters using an optimised latin hypercube. The values 
    are saved in dedicated columns of an excel document.

    Reads    

    Parameters:
        - settings: A Settings object containing the settings for the DIC analysis.
        - resultsFile: The name of the file to store the results in.
        - externalRay: A boolean indicating whether to use an external ray server or not.
        - guiThread: The GUI thread object if running from the GUI, otherwise None. Used to 
                    cleanly stop the analysis if requested from the GUI.

    Returns:
        - returnData (list): A list of subSetPoint arrays. Each subSetPoint array is a
            3D matrix where the first plane contains the x-coordinates
            the second plane the y-coordinates and the remaining planes the subset size,
            shapeFn, CZNSSD value and model coefficients.  This array can be processed to
            obtain displacement and strain data and to generate graphs.

    Raises:
        - ValueError: If an invalid optimization algorithm is specified.
    """ 

    # Create a list of letter characters to access the correct excel columns
    # ord(R) = 82 ord(X) = 88 -> ASCII table
    letters = [chr(i) for i in range(ord('R'), ord('X') + 1)]

    # Generate normalised factor values based on the passed dirctionary 
    lhs_samples = lhs(len(parameter_dictionary), 
                      samples=number_of_samples, 
                      criterion='maximin',  
                      iterations=100)
    
    scaled_lhs = np.zeros_like(lhs_samples)

    # Each row = sample instance, each column = parameter
    lhs_values = np.zeros((number_of_samples, len(parameter_dictionary)))
    try:
        # Write pattern parameters to the excel sheet if the path to the document has been provided 
        save_to_excel = (excel_save_path is not None) and (not random_image_save_folder)

        if save_to_excel:         
            try:
                workbook = openpyxl.load_workbook(excel_save_path)
                sheet = workbook.active
            except FileNotFoundError:
                os.makedirs(os.path.dirname(excel_save_path), exist_ok=True)
                workbook = openpyxl.Workbook()
                sheet = workbook.active
                workbook.save(excel_save_path)

            # Column headings for paramter values (R to Y only -> max 8 params)
            for k, param in enumerate(parameter_dictionary.keys()):
                if k >= len(letters):
                    raise ValueError("Too many parameters. Only supports up to 8 (columns R to Y).")
                sheet[f'{letters[k]}1'] = param

            # Save pattern generation indicator in the first row of the 'Y' column. 
            # This will be used by the optimiser
            # If the generator was a perlin noise with texture, the second row in 'Y' is also provided with a value
            # to indicate the texture function
            # Determine pattern generator type

            texture = None
            sheet['Y1'] = gen_option.lower()

            if gen_option.lower() == 'perlin':            # Perlin is a special case because it has texture functions
                texture = image_texture_function.lower()  # Save the texture as a string

                valid_textures = [
                    'none',
                    'thresholded', 
                    'sinusoidal', 
                    'bimodal',
                    'logarithmic', 
                    'cubic', 
                    'perlin_blobs'
                ]
                if texture not in valid_textures:
                    texture = None
                else:
                    sheet['Y2'] = texture 

        # Get the determined sample parameters in the correct scales
        for i, (param, (min_val, max_val)) in enumerate(parameter_dictionary.items()):
            # Scale values up from their normalised versions
            # Convert sample from [0,1) to [a,b), b > a 
            # (https://scipy.github.io/devdocs/reference/generated/scipy.stats.qmc.LatinHypercube.integers.html)
            scaled_lhs[:, i] = lhs_samples[:, i] * (max_val - min_val) + min_val
        
        # sample instances
        # Create an empty list to store even numbers to serve as prefixes for 
        # the generated images. The list will be returned by the function
        even_labels = []
        for i in range(number_of_samples):
            excel_index = i + 2
            even_label = i * 2
            even_labels.append(even_label)
            
            # Save the parameters associates with the current sample instance
            for j, param in enumerate(parameter_dictionary.keys()):
                value = scaled_lhs[i, j]        # Get the scaled parameter values
                lhs_values[i,j] = value         # Store the value in the object that will be returned
                if save_to_excel:
                    sheet[f'{letters[j]}{excel_index}'] = value 

        if save_to_excel:
            workbook.save(excel_save_path)   

    except Exception as e:
        error_traceback = traceback.format_exc()
        print(f"Error Message: {str(e)}")
        print(f"Traceback: {error_traceback}")

    return even_labels, lhs_values


