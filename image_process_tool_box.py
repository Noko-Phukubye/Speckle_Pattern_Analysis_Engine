def meanSE(large_matrix, subset_size, stride):
    """
    Calculate the Mean Subset Entropy (meanSE) of an image.
    Reference: Yaofeng and Pang, 2007. 

    Description:
    - Subsets of the input matrix are created based on the given size and stride.
    - Within each subset, smaller 3x3 subsets are extracted.
    - The absolute intensity differences between the center point of each 3x3 subset
      and its surrounding points are computed and summed.
    - The summed values are normalized using the subset dimensions and pixel depth.
    - The entropy of all subsets is averaged to compute the Mean Subset Entropy.
    Parameters:
        large_matrix (ndarray): Input grayscale image.
        subset_size (int): Size of the main subsets.
        stride (int): Step size for moving the subsets.
    Returns:
        float: Mean subset entropy.
    """

    # I have not used this one in 
    # a long time and I wrote it in 2024 so it might be wrong. 
    # Just doublecheck
    # .
    M = subset_size
    N = subset_size
    submatrices = img_subsets(large_matrix, subset_size, stride)
    # Flatten to get a list of submatrices
    submatrices_list = submatrices.reshape(-1, M, N)
    # Subsets within subset
    mini_subsetsize = 3
    small_ss = []

    for submatrix in submatrices_list:
        submatrices_3x3 = sliding_window_view(submatrix, (mini_subsetsize, mini_subsetsize))
        small_ss.append(submatrices_3x3.reshape(-1, mini_subsetsize, mini_subsetsize))

    # Creating a list of all extracted 3x3 mini_subsets
    submatrices_3x3 = np.concatenate(small_ss, axis=0)

    # Calculate difference between the center (P) and surrounding values
    beta = 8
    center_index = mini_subsetsize // 2
    center_values = submatrices_3x3[:, center_index, center_index]
    difference = np.abs(submatrices_3x3 - center_values[:, np.newaxis, np.newaxis])

    # Calculate entropy
    delta = np.sum(difference, axis=(1, 2)) / (M * N * (2**beta))
    mean_subset_entropy = np.mean(delta)

    return mean_subset_entropy


