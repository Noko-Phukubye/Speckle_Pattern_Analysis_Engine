def swap_image_pairs(ref_dir, def_dir, file_suffix="_Generated_spec_image.tif"):
    """
    Swaps image pairs between reference and deformed directories to correct DIC analysis setup,
    without changing the filenames. As of 20/05/2025 it seems it might not be needed anymore.
    
    Args:
        ref_dir (str): Path to reference images directory
        def_dir (str): Path to deformed images directory
        file_suffix (str): Common suffix for image filenames
    """
    # Get all files in each directory with the given suffix
    # From the reference image folder
    ref_files = sorted([f for f in os.listdir(ref_dir) if f.endswith(file_suffix)])
    # From the deformed image folder
    def_files = sorted([f for f in os.listdir(def_dir) if f.endswith(file_suffix)])

    if len(ref_files) != len(def_files):
        print("Warning: Unequal number of matching files")
        return

    # Create temporary names to avoid overwrite during move
    for ref_file, def_file in zip(ref_files, def_files):
        ref_path = os.path.join(ref_dir, ref_file)
        def_path = os.path.join(def_dir, def_file)

        temp_ref_path = ref_path + ".tmp"
        temp_def_path = def_path + ".tmp"

        # Rename to temporary files
        os.rename(ref_path, temp_ref_path)
        os.rename(def_path, temp_def_path)

    # Move them to the opposite folders
    for ref_file, def_file in zip(ref_files, def_files):
        temp_ref_path = os.path.join(ref_dir, ref_file + ".tmp")
        temp_def_path = os.path.join(def_dir, def_file + ".tmp")

        shutil.move(temp_ref_path, os.path.join(def_dir, ref_file))  # ref -> def
        shutil.move(temp_def_path, os.path.join(ref_dir, def_file))  # def -> ref

    print(f"Successfully swapped {len(ref_files)} image pairs between '{ref_dir}' and '{def_dir}'")


