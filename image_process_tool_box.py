def write_spec_excel(excel_path, p_metrics, meas_error, p_param, nans, indicators):
    """
    Write pattern evaluation data to Excel file.
    
    Parameters:
    -----------
    excel_path : str
        Full path where Excel file will be saved
    p_metrics : ndarray
        Pattern metrics array (n_rows x 8)
    meas_error : ndarray
        Measurement error array (n_rows x 8)
    p_param : ndarray
        Pattern parameters array (n_rows x 5)
    nans : ndarray
        NaN indicators array (n_rows x 1)
    indicators : ndarray
        Metadata indicators (3 x 1)
    """
    
    # Create new workbook and get active sheet
    wb = openpyxl.Workbook()
    sheet = wb.active
    
    # Define column headers for each data type
    metric_headers = ['MSF', 'MIG', 'E_f', 'MIOSD', 'Shannon entropy', 'Power area', 'SSSIG', 'R_peak']
    error_headers = ['RMSE(dic)', 'MAPE(dic)', 'RMSE(fe)', 'MAPE(fe)', 'Mean error(dic)', 'Mean error(fe)', 'Std dev(dic)', 'Std dev(fe)']
    param_headers = ['Speckle diameter', 'Speckle density', 'Position randomness', 'Grid step', 'Size randomness']
    
    # Write metric headers (columns 1-8)
    for j, header in enumerate(metric_headers):
        sheet.cell(row=1, column=j+1).value = header
    
    # Write error headers (columns 9-16)
    for l, header in enumerate(error_headers):
        sheet.cell(row=1, column=9+l).value = header
    
    # Write parameter headers (columns 18-22)
    for k, header in enumerate(param_headers):
        sheet.cell(row=1, column=18+k).value = header
    
    # Write indicator metadata (column 25)
    sheet.cell(row=1, column=25).value = indicators[0, 0]  # First indicator
    sheet.cell(row=2, column=25).value = indicators[1, 0]  # Second indicator
    
    # Write NaN column header (column 26)
    sheet.cell(row=1, column=26).value = 'NaNs'
    
    # Check if Turing parameters exist in the data
    if isinstance(indicators[2, 0], dict) and indicators[2, 0].get('exists'):
        # Write Turing parameter headers (columns 23-24)
        sheet.cell(row=1, column=23).value = 'Turing Radius'
        sheet.cell(row=1, column=24).value = 'Turing Reps'
    
    # Write all data rows (starting from row 2)
    for i in range(len(p_metrics)):
        row_num = i + 2  # Row 1 is headers, data starts at row 2
        
        # Write pattern metrics (columns 1-8)
        for j in range(8):
            sheet.cell(row=row_num, column=j+1).value = p_metrics[i, j]
        
        # Write measurement errors (columns 9-16)
        for l in range(8):
            sheet.cell(row=row_num, column=9+l).value = meas_error[i, l]
        
        # Write pattern parameters (columns 18-22)
        for k in range(5):
            sheet.cell(row=row_num, column=18+k).value = p_param[i, k]
        
        # Write NaN indicator (column 26)
        sheet.cell(row=row_num, column=26).value = nans[i, 0]
        
        # Write Turing parameters if they exist (columns 23-24)
        if isinstance(indicators[2, 0], dict) and indicators[2, 0].get('exists'):
            sheet.cell(row=row_num, column=23).value = indicators[2, 0]['radius'][i, 0]
            sheet.cell(row=row_num, column=24).value = indicators[2, 0]['reps'][i, 0]
    
    # Save the workbook to specified path
    wb.save(excel_path)
    print(f"Excel file saved to: {excel_path}")



