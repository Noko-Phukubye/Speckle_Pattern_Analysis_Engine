def plot_error_kdes(errors_dir, file_numbers, save_loc=None, 
                    output_filename='combined_kde_plot.png',
                    xlim=(-0.05, 0.05), ylim=(0, 300), bins=1000, top = 3):
    """
    Loads specified errors2 .npy files, computes KDEs, and plots them on a single figure
    Ranks the results according to height to standard deviation ratio

    Parameters:
        errors_dir (str): Directory where the errors2_*.npy files are located.
        file_numbers (list of int): List of pattern file numbers to load (e.g., [1, 2, 3]).
        output_filename (str): Name of the output plot file (saved in errors_dir).
        xlim (tuple): x-axis limits for the plot.
        ylim (tuple): y-axis limits for the plot.
        bins (int): Number of points in the KDE evaluation grid.

    Returns:
        None
    """

    # IEEE plotting standards
    plt.style.use(['science', 'no-latex','ieee','high-vis', 'grid'])

    plt.rcParams.update({
        'font.family': 'DejaVu Sans',
        'font.size': 28,
        'axes.labelsize': 26,
        'xtick.labelsize': 24,
        'ytick.labelsize': 24,
        'legend.fontsize': 24,
        'lines.linewidth': 3.0,
        'lines.markersize': 5,

        # Border styling
        'axes.linewidth': 2.25,     
        'xtick.major.width': 3.0,    
        'ytick.major.width': 3.0,    
        'xtick.minor.width': 1.5,    
        'ytick.minor.width': 1.5,

        'xtick.major.size': 10.0,      
        'ytick.major.size': 10.0,
        'xtick.minor.size': 5.0,      
        'ytick.minor.size': 5.0,
    })

    # Start figure
    plt.figure(figsize=(12, 10))

    # Line styles and colours
    # linestyles = ['-', '--', '-.', ':']
    # colors = plt.cm.tab10.colors

    # List object for storing IQR
    iqrange = []

    # Loop through numpy files containing already calculated errors
    for idx, file_number in enumerate(file_numbers):
        file_path = os.path.join(errors_dir, f"{file_number}_errors.npy")

        if not os.path.isfile(file_path):
            print(f"File not found: {file_path}")
            continue

        errors2 = np.load(file_path)
        data = errors2[~np.isnan(errors2)].flatten()

        if data.size == 0:
            print(f"No valid data in file: {file_path}")
            continue
        
        # Where the magic happens. Continuous histograms from the discrete data
        kde = gaussian_kde(data)
        x_vals = np.linspace(xlim[0], xlim[1], bins)
        y_vals = kde(x_vals)

        # Assign style and colour
        # color = colors[idx % len(colors)]
        # linestyle = linestyles[idx % len(linestyles)]
        
        # Figure labels - use 1-indexed for display
        lab = f'Optimised {idx + 1}'
        plt.plot(x_vals, y_vals, 
                 label=lab, 
                #  color=color, 
                #  linestyle=linestyle
                 )

        # Get standard deviation and peak values
        IQRange = stats.iqr(data)

        # Append the values to the lists
        iqrange.append((file_number, IQRange))

    # Finalise plot
    plt.xlabel('Error [pixel]')
    plt.ylabel('Density')
    plt.tick_params(axis='both')
    plt.xlim(*xlim)
    plt.ylim(*ylim)
    plt.legend()  # Use automatic legend from labels

    # Save figure
    if save_loc is None:
        output_path = os.path.join(errors_dir, output_filename)
    else:
        output_path = os.path.join(save_loc, output_filename)

    plt.savefig(output_path)
    plt.close()

    # Order results in ascending
    Ordered_scores = sorted(iqrange, key=lambda x: x[1], reverse=False)[:top]

    # Store in list that does not store duplicates
    seen = set()
    unique_ordered_scores = []

    for fnum, score in Ordered_scores:
        if score not in seen:
            seen.add(score)         # add if not already there
            unique_ordered_scores.append((fnum, score))

    print("\nTop patterns (IQR):")
    for i, (fnum, score) in enumerate(unique_ordered_scores, 1):
        print(f"{i}. Pattern {fnum} — iqr = {score:.5f}")

