# Mean intensity of second derivative
#---------------------------------------------------------------------------
def miosd(image2):
    """
    Calculate the Mean Intensity of the Second Derivative (MIOSD) using the second derivative of the image.
    Reference: Yu et al, 2014
    Parameters:
        image2 (ndarray): Input image (grayscale).
    Returns:
        float: The MIOSD value for the image based on second derivatives.
    """
    # Store the size of the image
    nY = image2.shape[0]
    nX = image2.shape[1]
    
    # Calculate the second derivatives with respect to rows and columns
    # axis = 0 -> Down vertically
    # axis = 1 -> Across horizontally
    d2fRows = np.gradient(np.gradient(image2, axis=0), axis=0)  
    d2fCols = np.gradient(np.gradient(image2, axis=1), axis=1)
    # Calculate the L2 norm of the second derivatives
    d2f = np.sqrt(d2fRows**2 + d2fCols**2)
    # Calculate the MIOSD value
    moisd = np.sum(d2f) / (nY * nX)

    return moisd


