# Mean subset fluctuation
#---------------------------------------------------------------------------
def MSF(image):
    """
    Calculate the Mean Subset Fluctuation (MSF) for an image.
    Reference: Hua et al., 2011.
    Parameters:
        image (ndarray): Input grayscale image.
    Returns:
        float: The MSF value for the image.
    Description:
    - The image is divided into overlapping 3x3 pixel subsets.
    - For each subset, the mean pixel intensity is calculated.
    - The absolute differences between the subset's pixel intensities and the mean value are computed.
    - These differences are summed for each subset and then aggregated across all subsets.
    - The final value is normalized by the total number of pixels in the image.
    """
    if image.ndim > 2:
        image = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    
    H, W = image.shape
    
    # Get all subsets at once
    submatrix = img_subsets(image, 3, 1)  # shape: (H-2, W-2, 3, 3)
    
    # Vectorized computation
    # Calculate mean for each subset along last two axes
    subset_means = np.mean(submatrix, axis=(2, 3), keepdims=True)
    
    # Calculate absolute deviations and sum
    fluctuations = np.abs(submatrix - subset_means)
    total_fluctuation = np.sum(fluctuations)
    
    return total_fluctuation / (H * W)

