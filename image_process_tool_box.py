# Generate a reference/deformed pair of Perlin noise images
#-------------------------------------------------------------
def generate_perlin_pair(h, w, dx_field, dy_field, excel_path, ref_image_save, 
                         def_image_save,
                         number_of_images=1, save_to_random_folder=False, seed=None, 
                         texture_fun="none"):
    """
    Adapted from: https://www.gmschroeder.com/blog/intro_pyart1.html

    Generates a pair of images: an undeformed (template) and a deformed 
    version using Perlin noise,
    with displacements applied through RBFInterpolator fields. Also applies 
    texture functions to both images.

    Parameters:
        h, w            : Image height and width
        dx_field        : RBFInterpolator object for x displacements
        dy_field        : RBFInterpolator object for y displacements
        excel_path      : Path to save parameter values
        ref_image_save  : Path to save reference images
        def_image_save  : Path to save deformed images
        number_of_images: Number of image pairs to generate
        save_to_random_folder: Whether to save to a random folder
        seed            : For reproducibility
        texture_type    : Type of texture to apply ("none", "leopard", "sinusoidal", etc.)
        texture_scale   : Scale parameter for texture functions that need it

    Returns:
        None (saves images to disk)
    """
    # Parameter dictionary, for scale, octaves, persistence, and lacunarity
    perlin_dictionary = {
        "scale": (0.0001, 0.4),         
        "octaves": (1, 8),            
        "persistence": (0.005, 1.0),   
        "lacunarity": (0.1, 8.0),      
    }

    # Generate parameters using latin-hypercube sampling
    even_list, perlin_lhs_values = gen_lhs_parameters_and_save(perlin_dictionary, 
                                                               number_of_images, 
                                                               excel_path, 
                                                               save_to_random_folder,
                                                                gen_option='perlin',
                                                                image_texture_function=texture_fun)
    
    print(f'\nMatrix of Perlin noise parameters: {perlin_lhs_values}')

    print(f'\nGenerating Perlin image pairs with texture type: {texture_fun}...\n')

    # Create coordinate grid using image dimensions
    x, y = np.meshgrid(np.arange(w), np.arange(h))
    coords = np.stack([x.flatten(), y.flatten()], axis=-1)

    # Get displacement fields. Evaluated at coordinate array locations.
    dx_vals = dx_field(coords).reshape(h, w)
    dy_vals = dy_field(coords).reshape(h, w)

    # Create the inverse displacement function d⁻¹(x,y)
    # For each point (x,y) in the deformed image, d⁻¹(x,y) gives the position in the original space
    x_inv = x - dx_vals
    y_inv = y - dy_vals

    for sample_idx in range(len(perlin_lhs_values[:,0])):
        
        tick0 = time.time()

        if seed is not None:
            np.random.seed(seed)
        
        # Build noise
        scale = perlin_lhs_values[sample_idx, 0]
        octaves = int(round(perlin_lhs_values[sample_idx, 1]))
        persistence = perlin_lhs_values[sample_idx, 2]
        lacunarity = perlin_lhs_values[sample_idx, 3]
        base_seed = seed or 0
                
        # Generate template image using Perlin noise
        template = np.zeros((h, w), dtype=np.float32)
        for row in range(h):
            for col in range(w):      
                # Sample noise at each integer pixel location
                template[row, col] = pnoise2(col * scale, row * scale,
                                        octaves=octaves,
                                        persistence=persistence,
                                        lacunarity=lacunarity,
                                        base=base_seed)
        

        # Generate deformed image using the inverse mapping: 
        # new_pixel_x, new_pixel_y = noise(d⁻¹(old_pixel_x, old_pixel_y))
        deformed = np.zeros((h, w), dtype=np.float32)
        for row in range(h):
            for col in range(w):
                # Get the inverse mapped point by applying inverse-deformed grid coordinates
                # when sampling the Perlin noise.
                # Because of inverse deformation, the pixel locations may not be integers anymore
                # but thats fine as the noise can be sampled from anywhere in 2D space
                src_x = x_inv[row, col]
                src_y = y_inv[row, col]
                
                # Evaluate the noise function at the inverse mapped point
                deformed[row, col] = pnoise2(src_x * scale, src_y * scale,
                                    octaves=octaves,
                                    persistence=persistence,
                                    lacunarity=lacunarity,
                                    base=base_seed)
        

        # Normalise images to [0, 255] 
        # As unsigned integer 8-bit
        # (FYI: noise is theoretically on [-1,1] but ive seen that its usually [-0.7,0.7])
        global_min = min(template.min(), deformed.min())
        global_max = max(template.max(), deformed.max())
        template = np.round((template - global_min) / (global_max - global_min) * 255).astype(np.uint8)
        deformed = np.round((deformed - global_min) / (global_max - global_min) * 255).astype(np.uint8)


        # Apply texture functions
        textured_template = apply_texture(template, texture_type=texture_fun)
        textured_deformed = apply_texture(deformed, texture_type=texture_fun)
        print(f'Time taken apply texture: {(time.time() - tick0):.3f}')

        # Save the textured images
        spec_file_path = os.path.join(ref_image_save, f'{even_list[sample_idx]}_Generated_spec_image.tif')
        def_file_path = os.path.join(def_image_save, f'{even_list[sample_idx]+1}_Generated_spec_image.tif')

        cv2.imwrite(spec_file_path, textured_template)
        cv2.imwrite(def_file_path, textured_deformed)
  
        print(f'{even_list[sample_idx]}_Generated_spec_image.tif saved')
        print(f'{even_list[sample_idx] + 1}_Generated_spec_image.tif saved\n-------------------------')


