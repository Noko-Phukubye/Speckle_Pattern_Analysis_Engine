def img_deform(image, source_points=None, 
               destination_points=None, kern=3, dx=None, dy=None):
    """
    Deforms an image using a displacement field generated by 
    Radial Basis Function (RBF) interpolation.

    If dx and dy are not provided, the displacement field is 
    computed from source and destination points 
    (which must be provided in that case).
    The deformation is then applied using scipy.ndimage.map_coordinates.

    Based on the approach in Rossi et al. (2015): 
    'Effect of DIC Spatial Resolution, Noise and Interpolation 
    Error on Identification Results with the VFM'.

    Parameters:
        image (ndarray): Input image (grayscale or RGB).
        source_points (ndarray): Nx2 array of original coordinates 
        (optional if dx/dy are provided).
        destination_points (ndarray): Nx2 array of deformed coordinates 
        (optional if dx/dy are provided).

        kern (int): Kernel type for RBF interpolation:
                    1 = thin_plate_spline, 2 = linear, 3 = cubic, 4 = quintic.
        dx (ndarray): Precomputed x-displacement field (optional).
        dy (ndarray): Precomputed y-displacement field (optional).

    Returns:
        remapped_image (ndarray): Deformed image.
        difference_image (ndarray): Absolute pixel-wise difference from original.
    """
    h, w = image.shape[:2]

    # Create pixel coordinate grid
    grid_x, grid_y = np.meshgrid(np.arange(w), np.arange(h))
    grid_x_flatten = grid_x.flatten()
    grid_y_flatten = grid_y.flatten()

    # Generate displacement field if not provided
    if dx is None and dy is None:
        kernel_options = {
            1: 'thin_plate_spline',
            2: 'linear',
            3: 'cubic',
            4: 'quintic'
        }

        kerne = kernel_options.get(kern)
        print("Running radial basis function interpolation with kernel:", kerne)

        if kerne is None:
            raise ValueError(
                "Invalid kernel option. Use 1 (thin_plate_spline), 2 (linear), 3 (cubic), or 4 (quintic)."
            )

        # Create interpolator objects for each direction
        rbf_interpolator_x = RBFInterpolator(
            source_points,
            destination_points[:, 0] - source_points[:, 0],
            kernel=kerne
        )
        rbf_interpolator_y = RBFInterpolator(
            source_points,
            destination_points[:, 1] - source_points[:, 1],
            kernel=kerne
        )

        # Evaluate interpolators over full image grid
        grid_stack = np.column_stack([grid_x_flatten, grid_y_flatten])
        dx = rbf_interpolator_x(grid_stack).reshape(h, w)
        dy = rbf_interpolator_y(grid_stack).reshape(h, w)

    # Ensure displacement field exists
    if dx is None and dy is None and destination_points is None:
        raise Exception('Image deform error: Displacement field not provided.')

    # Compute remapped pixel coordinates
    new_x = grid_x - dx
    new_y = grid_y - dy
    new_coordinates = np.array([new_y, new_x])

    print("Remapping image using RBF-based transformation and map_coordinates...")

    # Prepare output image container
    remapped_image = np.zeros_like(image)
    ordr = 5

    # Remap grayscale image
    if len(image.shape) == 2:

        remapped_image = map_coordinates(
            image,
            new_coordinates,
            order=ordr,
            mode='constant',
            prefilter=True
        )
    # loop through and remap each channel for color image
    else:
        for i in range(image.shape[2]):
            # All preceding index values (all x and y) evaluated for 
            # each channel (i).
            remapped_image[..., i] = map_coordinates(
                image[..., i],
                new_coordinates,
                order=ordr,
                mode='constant',        # This seems to have litte affect on the edge artefact problem
                prefilter=True
            )

    # Compute absolute pixel-wise difference
    original_float = image.astype(np.float32)
    remapped_float = remapped_image.astype(np.float32)
    difference_image = np.abs(original_float - remapped_float)

    return remapped_image, difference_image



