# Shannon entropy
#----------------------------------------------------------------------------------------------
def ShannonEnt(img):
    """
    Credit: https://github.com/zhaoxuhui/Entropy/blob/master/python/entropy.py
    """
    entropy = []
    hist = cv2.calcHist([img], [0], None, [256], [0, 255])
    total_pixel = img.shape[0] * img.shape[1]

    for item in hist:
        probability = item / total_pixel
        if probability == 0:
            en = 0
        else: 
            en = -1 * probability * (np.log(probability) / np.log(2))
        entropy.append(en)
    arr = np.asarray(entropy, dtype="object")
    sum_en = np.sum(arr)
    return float(sum_en.item()) 


