def autocorr(image,dx_min = -40, dx_max = 40, meth = 6, cardinality = 100, autype = '2d'):

    """
    Function uses openCV's template matching to generate an autocorrelation
    graph of an image. The function also returns an array of 
    peak autocorrelation values and their associated pixel displacements.

    Input
        image : ndarry Image to be correlated with itself. Does not need to be grayscale
        dx_min: integer representing the lower limit of pixels to which the image will be shifted
        dx_max: integer representing the upper limit of pixels to which the image will be shifted
        type: string indicating whether the correlation surface should be in 2D or 3D
        meth: Select the autocorrelation method (e.g. cross correlation, sum of squared differences, etc)
        cardinality: The resolution of displacements used for creating the autocorrelation curves defined by number of points
                     between the minimum and maximum displacements.


    Output
        Figure: The resulting autocorrelation distribution in 2D or 3D
        Value: array of peak values and their associated applied displacements
        R_peak: Autocorrelation peak sharpness radius. proposed by S. Bossuyt, (2013). 
                R_peak = sqrt((A_inf - A_0) / (<A_+1> - A_0)) 
                where A_0 is the autocorelation value for zero displacement
                A_+1 is the mean of the autocorrelation at a displacement of 1 pixel in each of the 4 cardinal directions
                A_inf is the autocorrelation value at infinite displacement (basically zero, according to )
    """

    # Track time taken
    time1 = time.time()

    # Methods dictionary 
    methods = dict([
        (1, 'TM_CCOEFF'),
        (2, 'TM_CCOEFF_NORMED'),
        (3, 'TM_CCORR'),
        (4, 'TM_CCORR_NORMED'),
        (5, 'TM_SQDIFF'),
        (6, 'TM_SQDIFF_NORMED')
        ])
    
    # Choose correlation method
    method = getattr(cv2, methods.get(meth))
    print("\nAutocorrelation analysis: method", methods.get(meth))

    # Turn image grayscale if it isn't already (redundent now 23/04/2025)
    if image.ndim > 2:
        img = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    else:
        img = image

    # Peak radius calculation according to Bossuyt (2013). Move 1 pixel in all 4 directions.
    card_shift_px = translate_image(img,1,0)
    card_shift_nx = translate_image(img,-1,0)
    card_shift_py = translate_image(img,0,1)
    card_shift_ny = translate_image(img,0,-1)
    # Correlate all shifted images to the original
    A_px = np.max(cv2.matchTemplate(img, card_shift_px, int(method)))
    A_nx = np.max(cv2.matchTemplate(img, card_shift_nx, int(method)))
    A_py = np.max(cv2.matchTemplate(img, card_shift_py, int(method)))
    A_ny = np.max(cv2.matchTemplate(img, card_shift_ny, int(method)))
    A_0 = np.max(cv2.matchTemplate(img, img, int(method)))              # No-shift correlation

    # Calculate A_inf (background correlation level at large displacements)
    large_shift = max(abs(dx_min), abs(dx_max)) // 2  # Use half of maximum displacement to evaluate at large displacements
    A_inf_shifts = [large_shift, -large_shift]
    A_inf_values = []

    # Shift the images and correlate it with the unshifted version. Append the maximum correlation value
    # to the list.

    for shift in A_inf_shifts:
        template = translate_image(img, shift, 0)
        res = cv2.matchTemplate(img, template, int(method))
        A_inf_values.append(np.max(res))

    A_inf = np.mean(A_inf_values)

    A_plusmin = np.mean(np.array((A_px,A_nx,A_py,A_ny)))
    R_peak = np.sqrt((A_inf - A_0) / (A_plusmin - A_0))

    # Pixel displacements. This is for plotting the autocorrelation curve.
    # For 2D and 3D 
    image_shifts = np.linspace(dx_min,dx_max,cardinality)

    # plot figure using the IEEE academic standard
    plt.style.use(['science', 'no-latex','ieee', 'grid'])

    plt.rcParams.update({
        'font.family': 'DejaVu Sans',
        'font.size': 8,
        'axes.labelsize': 8,
        'xtick.labelsize': 8,
        'ytick.labelsize': 8,
        'legend.fontsize': 8,
        'lines.linewidth': 3.5,
        'lines.markersize': 3
    })


    if autype.lower() == '2d':
        correlation_value = np.zeros(len(image_shifts))

        # Find mean correlation value at each shift
        for i, shift in enumerate(image_shifts):
            template = translate_image(img,shift,0)
            res = cv2.matchTemplate(img, template, int(method))
            correlation_value[i] = np.mean(res)

        # Invert for sum of squared differences to keep 1 as maximum correlation
        if meth == 6:
            # newval = 1-correlation_value
            newval = correlation_value

            figure = plt.figure(figsize=(6,6))
            plt.plot(image_shifts,newval, color='black',  linewidth=1)
            plt.xlabel('Relative displacements (pixels)')
            plt.ylabel('ZNSSD')
            plt.xlim(dx_min, dx_max)
            plt.ylim(-0.05, 1.05)
        else:
            figure = plt.figure(figsize=(6,6))
            plt.plot(image_shifts,correlation_value, color='black',  linewidth=1)
            plt.xlabel('Relative displacements (pixels)')
            plt.ylabel('Cross correlation')
            plt.xlim(dx_min, dx_max)
            plt.ylim(0, 1.1)
            
        time2 = time.time()
        print(f'\nTime taken: {time2-time1}\n')
        return correlation_value, figure, R_peak
    
    # region 3D case (I think I only used it once in the project.)
    if autype.lower() == '3d':
        correlation_value = np.zeros((len(image_shifts),len(image_shifts)))
        for i,shift_x in enumerate(image_shifts):
            for j,shift_y in enumerate(image_shifts):

                template = translate_image(img,shift_x,shift_y)
                res = cv2.matchTemplate(img, template, int(method))
                correlation_value[i,j] = np.max(res)
        dx,dy = np.meshgrid(image_shifts,image_shifts)

        fig = plt.figure(figsize=(8,5))
        ax = fig.add_subplot(111,projection='3d')
        surf = ax.plot_surface(dx, dy, correlation_value, cmap='jet', linewidth=0, antialiased=True)
        fig.colorbar(surf,ax=ax, shrink=0.5, aspect=5)
        ax.set_xlabel('X Displacement (pixels)')
        ax.set_ylabel('Y Displacement (pixels)')
        ax.set_zlabel('Correlation Value')
        ax.set_title('Autocorrelation surface')
        ax.grid(True)
        time2 = time.time()
        print(f'\nTime taken: {time2-time1}\n')
    # endregion

        return correlation_value,fig, R_peak
    else:
        print("Invalide input in 'type'. Options: '2D' or '3D'")   
        return None

